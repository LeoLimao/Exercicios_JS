/*   Elaborar um programa que efetue a leitura de três valores (A, B e C) e apresente como resultado final à
soma dos quadrados dos três valores lidos.*/

a=parseFloat(prompt("Digite o valor de A"))
b=parseFloat(prompt("Digite o valor de B"))
c=parseFloat(prompt("Digite o valor de C"))

soma=(a*a)+(b*b)+(c*c)

alert("O resultado da soma dos quadrados dos valores apresentados é "+soma+".")
