/*  Elaborar um programa que calcule e apresente o volume de uma caixa retangular, por meio da fórmula
VOLUME ← COMPRIMENTO * LARGURA * ALTURA.
 */

comprimento=parseInt(prompt("Por favor digite o comprimento da caixa"))
largura=parseInt(prompt("Por favor digite a largura da caixa"))
altura=parseInt(prompt("Por favor digite a altura da caixa"))
volume=comprimento*altura*largura

alert("O volume da caixa é de "+volume+"m³")
