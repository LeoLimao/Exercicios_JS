/*  Calcular e apresentar o valor do volume de uma lata de óleo, utilizando a fórmula:
Volume Raio <- 3,14² * Altura */

altura=parseFloat(prompt("Qual a altura desta lata de óleo?"))
raio=parseFloat(prompt("Qual o raio da desta lata de óleo?"))
volume=3,14*(raio*raio)*altura

alert("O volume dessa lata de óleo é de: "+volume+ "m².")