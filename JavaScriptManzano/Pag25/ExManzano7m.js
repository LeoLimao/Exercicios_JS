/*   Elaborar um programa que efetue a leitura de três valores (A,B e C) e apresente como resultado final o
quadrado da soma dos três valores lidos.*/

a=parseFloat(prompt("Digite o valor de A"))
b=parseFloat(prompt("Digite o valor de B"))
c=parseFloat(prompt("Digite o valor de C"))

soma=(a+b+c)*(a+b+c)

alert("O resultado do quadrado da somas dos valores apresentados é "+soma+".")
