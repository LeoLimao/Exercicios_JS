/*  Ler uma temperatura em graus Celsius e apresentá-la convertida em graus Fahrenheit. A fórmula de
conversão é F ← (9 * C + 160) / 5, sendo F a temperatura em Fahrenheit e C a temperatura em Celsius. */

C=parseFloat(prompt("Digite em graus Celsius para descobrir o valor em Fahrenheit"))
F=(9 * C + 160)/5

alert("A temperatura em Fahrenheit é de: "+F+ " graus.")